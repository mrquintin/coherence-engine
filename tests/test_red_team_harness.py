def _cli_env():
    env = os.environ.copy()
    env.setdefault("PYTHONPATH", str(REPO_ROOT))
    return env


def test_cli_red_team_run_exits_one_on_mismatch(tmp_path):
    """Current pinned behavior includes 8 mismatches -> exit 1."""
    out_path = tmp_path / "report.json"
    proc = subprocess.run(
        [
            sys.executable,
            "-m",
            "coherence_engine",
            "red-team-run",
            "--fixtures-dir",
            str(FIXTURES_DIR),
            "--labels",
            str(LABELS_PATH),
            "--output",
            str(out_path),
        ],
        cwd=str(REPO_ROOT),
        env=_cli_env(),
        capture_output=True,
        text=True,
    )
    assert proc.returncode == 1, (
        f"expected exit 1 (mismatch), got {proc.returncode}\n"
        f"stdout: {proc.stdout}\nstderr: {proc.stderr}"
    )
    assert out_path.exists()
    payload = json.loads(out_path.read_text(encoding="utf-8"))
    assert payload["mismatches"] == EXPECTED_TOTALS["mismatches"]
    assert payload["confusion_matrix"] == EXPECTED_CONFUSION_MATRIX


