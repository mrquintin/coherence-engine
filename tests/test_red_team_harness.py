def test_run_adversarial_suite_raises_on_missing_fixtures_dir(tmp_path):
    missing = tmp_path / "no_such_dir"
    with pytest.raises(RedTeamError):
        run_adversarial_suite(
            fixtures_dir=missing,
            labels_path=LABELS_PATH,
            policy_version=DECISION_POLICY_VERSION,
        )


def test_run_adversarial_suite_raises_on_missing_labels(tmp_path):
    missing_labels = tmp_path / "no_such_labels.json"
    with pytest.raises(RedTeamError):
        run_adversarial_suite(
            fixtures_dir=FIXTURES_DIR,
            labels_path=missing_labels,
            policy_version=DECISION_POLICY_VERSION,
        )


def test_run_adversarial_suite_raises_on_policy_version_mismatch():
    with pytest.raises(RedTeamError) as exc_info:
        run_adversarial_suite(
            fixtures_dir=FIXTURES_DIR,
            labels_path=LABELS_PATH,
            policy_version="decision-policy-v999",
        )
    assert "decision_policy_version_pin_mismatch" in str(exc_info.value)


def test_run_adversarial_suite_raises_on_unlabeled_fixture(tmp_path):
    # Copy a single fixture but provide an empty labels.json.
    fixture_src = next(FIXTURES_DIR.glob("*.json"))
    fx_dir = tmp_path / "fixtures"
    fx_dir.mkdir()
    (fx_dir / fixture_src.name).write_text(
        fixture_src.read_text(encoding="utf-8"), encoding="utf-8"
    )
    labels = tmp_path / "labels.json"
    labels.write_text("{}", encoding="utf-8")
    with pytest.raises(RedTeamError):
        run_adversarial_suite(
            fixtures_dir=fx_dir,
            labels_path=labels,
            policy_version=DECISION_POLICY_VERSION,
        )


def test_load_labels_rejects_unknown_verdict(tmp_path):
    labels = tmp_path / "labels.json"
    labels.write_text(
        json.dumps({"foo.json": {"expected_verdict": "invest"}}),
        encoding="utf-8",
    )
    with pytest.raises(RedTeamError):
        load_labels(labels)


