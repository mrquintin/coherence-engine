def test_cli_red_team_run_exits_zero_when_all_labels_match(tmp_path):
    """If we relabel every fixture to its currently-observed actual verdict,
    the CLI should exit 0. This guards against the exit-code logic itself
    inverting in a regression."""
    report = run_adversarial_suite(
        fixtures_dir=FIXTURES_DIR,
        labels_path=LABELS_PATH,
        policy_version=DECISION_POLICY_VERSION,
    )
    aligned_labels = {
        c.fixture_filename: {
            "expected_verdict": c.actual_verdict,
            "rationale": "aligned-to-current-output for exit-code test",
        }
        for c in report.cases
    }
    aligned_path = tmp_path / "labels_aligned.json"
    aligned_path.write_text(json.dumps(aligned_labels), encoding="utf-8")

    proc = subprocess.run(
        [
            sys.executable,
            "-m",
            "coherence_engine",
            "red-team-run",
            "--fixtures-dir",
            str(FIXTURES_DIR),
            "--labels",
            str(aligned_path),
        ],
        cwd=str(REPO_ROOT),
        env=_cli_env(),
        capture_output=True,
        text=True,
    )
    assert proc.returncode == 0, (
        f"expected exit 0 (all match), got {proc.returncode}\n"
        f"stdout: {proc.stdout[:500]}\nstderr: {proc.stderr}"
    )
